class Ngitung:
     def __init__(self, a, b):
         self.a = a
         self.b = b
         
     def Penambahan(self):
        r = self.a + self.b
        return r

     def Pengurangan(self):
        r = self.a * self.b
        return r
    
     def Perkalian(self):
        r = self.a * self.b
        return r
    
     def Pembagian(self) :
        r = self.a / self.b
        return r
